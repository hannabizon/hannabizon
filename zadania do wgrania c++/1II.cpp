#include <iostream>
using namespace std;
int main ()
{

double a, b;
cout << "Podaj pierwszą liczbę: ";
cin >> a;
cout << "Podaj drugą liczbę: ";
cin >> b;

cout << a << " + " << b << " = " << a + b << endl;
cout << a << " - " << b << " = " << a - b << endl;
cout << a << " * " << b << " = " << a * b << endl;
cout << a << " / " << b << " = " << a / b << endl;

return 0;

}
